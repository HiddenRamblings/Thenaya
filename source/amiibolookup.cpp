int getNameByHexId(uint16_t id, char *name, int length) {
	auto res = amiiboList.find(id);
	if (res == amiiboList.end())
		return 0;
	const char *val = (*res).second;
	int len = strlen(val);
	if (len >= length)
		return 0;

	strcpy(name, val);
	return len;
}

